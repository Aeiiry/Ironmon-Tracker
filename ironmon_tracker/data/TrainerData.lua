		["VICTORY_ROAD_3F"] = { "TRAINER_COOLTRAINER_GEORGE", "TRAINER_COOLTRAINER_COLBY", "TRAINER_COOLTRAINER_CAROLINE", "TRAINER_COOLTRAINER_ALEXA", "TRAINER_COOL_COUPLE_RAY_TYRA", "TRAINER_COOL_COUPLE_RAY_TYRA" },
		["VIRIDIAN_FOREST"] = { "TRAINER_BUG_CATCHER_RICK", "TRAINER_BUG_CATCHER_DOUG", "TRAINER_BUG_CATCHER_SAMMY", "TRAINER_BUG_CATCHER_ANTHONY", "TRAINER_BUG_CATCHER_CHARLIE" },
	}

	local usefulInfo = {}
	for layoutName, trainerNameList in pairs(layoutNameToTrainerNames) do
		local route = {
			nameRaw = layoutName,
			mapId = layoutNameToId[layoutName], -- nil okay, fix later manually
			trainers = {},
		}
		for i, trainerName in ipairs(trainerNameList) do
			local trainer = {
				nameRaw = trainerName,
				index = i,
				trainerId = tostring(trainerNameToId[trainerName] or trainerName),
			}
			table.insert(route.trainers, trainer)
		end
		table.insert(usefulInfo, route)
	end

	local lines = {}
	local function printf(str, ...)
		table.insert(lines, string.format(str, ...))
	end
	table.sort(usefulInfo, function(a, b)
		if a.mapId and b.mapId then return a.mapId < b.mapId end
		return a.nameRaw < b.nameRaw
	end)

	for _, route in ipairs(usefulInfo) do
		printf("[\"%s\"] = {", route.nameRaw)
		printf("\tmapId = %s,", tostring(route.mapId))
		local trainerIds = {}
		for _, trainer in ipairs(route.trainers) do
			table.insert(trainerIds, trainer.trainerId)
		end
		printf("\ttrainers = { %s },", table.concat(trainerIds, ", "))
		printf("},")
	end

	TrainerData.writeLinesToFile(lines, "TrainerInfo.txt")
end

function TrainerData.writeLinesToFile(lines, filename)
	local file = io.open(FileManager.prependDir(filename or "Unnamed File.txt"), "w")
	if file ~= nil then
		for _, line in ipairs(lines or {}) do
			file:write(line .. "\n")
		end
		file:flush()
		file:close()
	end
end