function TrainerData.writeLinesToFile(lines, filename)
	local file = io.open(FileManager.prependDir(filename or "Unnamed File.txt"), "w")
	if file ~= nil then
		for _, line in ipairs(lines or {}) do
			file:write(line .. "\n")
		end
		file:flush()
		file:close()
	end
end